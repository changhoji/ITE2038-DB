CREATE TABLE user (
    user_id int,
    username varchar(15),
    password varchar(15),

    PRIMARY KEY (user_id)
);

CREATE TABLE building (
    building_id int,
    name varchar(10),
    admin varchar(6),
    rooms int,

    PRIMARY KEY (building_id)
);

CREATE TABLE room (
    room_id int,
    building_id int,
    occupancy int,

    PRIMARY KEY (room_id),
    FOREIGN KEY (building_id) REFERENCES building(building_id)
);

CREATE TABLE major (
    major_id int,
    name varchar(20),

    PRIMARY KEY (major_id)
);

CREATE TABLE lecturer (
    lecturer_id int,
    name varchar(25),
    major_id int,

    PRIMARY KEY (lecturer_id),
    FOREIGN KEY (major_id) REFERENCES major(major_id)
);

CREATE TABLE student (
    student_id int,
    name varchar(10),
    sex varchar(6),
    major_id int,
    lecturer_id int,
    year int,
    user_id int,
    state varchar(10),

    PRIMARY KEY (student_id),
    FOREIGN KEY (user_id) REFERENCES user(user_id),
    FOREIGN KEY (lecturer_id) REFERENCES lecturer(lecturer_id),
    FOREIGN KEY (major_id) REFERENCES major(major_id)
);

CREATE TABLE admin (
    user_id int,

    PRIMARY KEY (user_id),
    FOREIGN KEY (user_id) REFERENCES user(user_id)
);



CREATE TABLE course (
    course_id char(7),
    name varchar(20),
    credit int,

    PRIMARY KEY (course_id)
);


CREATE TABLE credits (
    credit_id int,
    student_id int,
    course_id char(7),
    year int,
    grade varchar(2),
    credit int,

    PRIMARY KEY (credit_id),
    FOREIGN KEY (student_id) REFERENCES student(student_id),
    FOREIGN KEY (course_id) REFERENCES course(course_id)
);

CREATE TABLE class (
    class_id int,
    class_no int,
    course_id char(7),
    major_id int,
    name varchar(20),
    credit int,
    year int,
    lecturer_id int,
    person_max int,
    opened int,
    room_id int,

    PRIMARY KEY (class_id),
    FOREIGN KEY (course_id) REFERENCES course(course_id),
    FOREIGN KEY (major_id) REFERENCES major(major_id),
    FOREIGN KEY (lecturer_id) REFERENCES lecturer(lecturer_id),
    FOREIGN KEY (room_id) REFERENCES room(room_id)
);

CREATE TABLE time (
    time_id int AUTO_INCREMENT,
    class_id int,
    period int,
    begin char(5),
    end char(5),

    PRIMARY KEY (time_id),
    FOREIGN KEY (class_id) REFERENCES class(class_id)
);

CREATE TABLE wish (
	student_id int,
    class_id int,
    
    PRIMARY KEY (student_id, class_id),
    FOREIGN KEY (student_id) REFERENCES student(student_id),
    FOREIGN KEY (class_id) REFERENCES class(class_id)
);

CREATE TABLE enroll (
	student_id int,
    class_id int,
    
    PRIMARY KEY (student_id, class_id),
    FOREIGN KEY (student_id) REFERENCES student(student_id),
    FOREIGN KEY (class_id) REFERENCES class(class_id)
);

